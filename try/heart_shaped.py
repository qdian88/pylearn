import turtle
turtle.setup(800,600,200,200)
turtle.ht()
turtle.penup()
turtle.fd(-50)
turtle.left(45)
turtle.fd(-50)
turtle.seth(135)
turtle.pensize(20)
turtle.pencolor("red")
turtle.fillcolor("red")
turtle.begin_fill()
turtle.pendown()
turtle.circle(-50,225)
turtle.seth(90)
turtle.circle(-50,225)
turtle.fd(121)
turtle.seth(135)
turtle.goto(-85,-35)
turtle.end_fill()
turtle.done()